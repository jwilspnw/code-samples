public class Indices {
    private int start, end;

    public Indices(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    public void setIndices(int start, int end) {
        this.start = start;
        this.end = end;
    }
}
